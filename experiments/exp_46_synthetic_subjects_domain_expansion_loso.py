class SyntheticSubjectVisualizer:
    """All visualizations for the synthetic subjects experiment."""

    def __init__(self, output_dir: Path, logger):
        self.output_dir = output_dir
        self.logger = logger
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def _save(self, fig, name: str):
        path = self.output_dir / f"{name}.png"
        fig.savefig(path, dpi=200, bbox_inches="tight")
        plt.close(fig)
        self.logger.info(f"Saved figure: {path}")

    # 1. VAE Reconstruction Quality
    def plot_vae_reconstruction(
        self, x_real: np.ndarray, x_recon: np.ndarray, labels: np.ndarray,
        class_ids: List[int], n_gestures: int = 5, n_channels: int = 3,
    ):
        """Show original vs reconstructed vs error for several gestures."""
        fig, axes = plt.subplots(
            n_gestures, 3, figsize=(15, 3 * n_gestures),
            sharex=True, sharey=False,
        )
        if n_gestures == 1:
            axes = axes[np.newaxis, :]
        fig.suptitle("VAE Reconstruction Quality", fontsize=14, y=1.01)

        shown_gestures = class_ids[:n_gestures]
        for row, g_id in enumerate(shown_gestures):
            mask = labels == g_id
            if not mask.any():
                continue
            idx = np.where(mask)[0][0]
            orig = x_real[idx]       # (C, T)
            rec = x_recon[idx]       # (C, T)
            err = np.abs(orig - rec)

            for ch in range(min(n_channels, orig.shape[0])):
                axes[row, 0].plot(orig[ch], alpha=0.7, label=f"Ch{ch}")
                axes[row, 1].plot(rec[ch], alpha=0.7)
                axes[row, 2].plot(err[ch], alpha=0.7)

            axes[row, 0].set_ylabel(f"Gesture {g_id}")
            if row == 0:
                axes[row, 0].set_title("Original")
                axes[row, 1].set_title("Reconstruction")
                axes[row, 2].set_title("|Error|")
            if row == 0:
                axes[row, 0].legend(fontsize=7, loc="upper right")

        fig.tight_layout()
        self._save(fig, "fig_vae_reconstruction")

    # 2. Latent Space t-SNE
    def plot_latent_space_tsne(
        self,
        mu_content: np.ndarray,
        mu_style: np.ndarray,
        gesture_labels: np.ndarray,
        subject_labels: np.ndarray,
        max_points: int = 3000,
    ):
        """2×2 t-SNE: content/style × gesture/subject coloring."""
        rng = np.random.RandomState(42)
        n = len(mu_content)
        if n > max_points:
            idx = rng.choice(n, max_points, replace=False)
            mu_content = mu_content[idx]
            mu_style = mu_style[idx]
            gesture_labels = gesture_labels[idx]
            subject_labels = subject_labels[idx]

        self.logger.info("Computing t-SNE for latent spaces...")
        tsne_c = TSNE(n_components=2, perplexity=30, random_state=42).fit_transform(mu_content)
        tsne_s = TSNE(n_components=2, perplexity=30, random_state=42).fit_transform(mu_style)

        fig, axes = plt.subplots(2, 2, figsize=(14, 12))
        fig.suptitle("Latent Space t-SNE (Disentanglement Check)", fontsize=14)

        unique_gestures = np.unique(gesture_labels)
        unique_subjects = np.unique(subject_labels)
        g_colors = plt.cm.tab20(np.linspace(0, 1, len(unique_gestures)))
        s_colors = plt.cm.Set3(np.linspace(0, 1, max(len(unique_subjects), 3)))

        # Content × gesture
        for i, g in enumerate(unique_gestures):
            mask = gesture_labels == g
            axes[0, 0].scatter(
                tsne_c[mask, 0], tsne_c[mask, 1],
                c=[g_colors[i]], s=8, alpha=0.6, label=f"G{g}",
            )
        axes[0, 0].set_title("Content space → Gesture")
        axes[0, 0].legend(fontsize=6, markerscale=2, ncol=2, loc="upper right")

        # Content × subject
        for i, s in enumerate(unique_subjects):
            mask = subject_labels == s
            axes[0, 1].scatter(
                tsne_c[mask, 0], tsne_c[mask, 1],
                c=[s_colors[i % len(s_colors)]], s=8, alpha=0.6, label=f"S{s}",
            )
        axes[0, 1].set_title("Content space → Subject")
        axes[0, 1].legend(fontsize=6, markerscale=2, loc="upper right")

        # Style × gesture
        for i, g in enumerate(unique_gestures):
            mask = gesture_labels == g
            axes[1, 0].scatter(
                tsne_s[mask, 0], tsne_s[mask, 1],
                c=[g_colors[i]], s=8, alpha=0.6,
            )
        axes[1, 0].set_title("Style space → Gesture")

        # Style × subject
        for i, s in enumerate(unique_subjects):
            mask = subject_labels == s
            axes[1, 1].scatter(
                tsne_s[mask, 0], tsne_s[mask, 1],
                c=[s_colors[i % len(s_colors)]], s=8, alpha=0.6, label=f"S{s}",
            )
        axes[1, 1].set_title("Style space → Subject")
        axes[1, 1].legend(fontsize=6, markerscale=2, loc="upper right")

        for ax in axes.flat:
            ax.set_xticks([])
            ax.set_yticks([])

        fig.tight_layout()
        self._save(fig, "fig_latent_space_tsne")

    # 3. Real vs Synthetic t-SNE
    def plot_real_vs_synthetic_tsne(
        self,
        features_real: np.ndarray,
        labels_real: np.ndarray,
        features_synth: np.ndarray,
        labels_synth: np.ndarray,
        max_points: int = 2000,
    ):
        """t-SNE of feature vectors: real (circles) vs synthetic (triangles)."""
        rng = np.random.RandomState(42)

        n_r = min(len(features_real), max_points)
        n_s = min(len(features_synth), max_points)
        idx_r = rng.choice(len(features_real), n_r, replace=False)
        idx_s = rng.choice(len(features_synth), n_s, replace=False)

        feats = np.concatenate([features_real[idx_r], features_synth[idx_s]], axis=0)
        labs = np.concatenate([labels_real[idx_r], labels_synth[idx_s]])
        is_synth = np.concatenate([
            np.zeros(n_r, dtype=bool),
            np.ones(n_s, dtype=bool),
        ])

        # Handle NaN/Inf
        feats = np.nan_to_num(feats, nan=0.0, posinf=0.0, neginf=0.0)

        self.logger.info("Computing t-SNE for real vs synthetic...")
        tsne = TSNE(n_components=2, perplexity=30, random_state=42).fit_transform(feats)

        fig, ax = plt.subplots(figsize=(10, 8))
        unique_labels = np.unique(labs)
        colors = plt.cm.tab20(np.linspace(0, 1, len(unique_labels)))

        for i, g in enumerate(unique_labels):
            # Real
            mask_r = (~is_synth) & (labs == g)
            ax.scatter(
                tsne[mask_r, 0], tsne[mask_r, 1],
                c=[colors[i]], s=20, alpha=0.5, marker="o",
                label=f"G{g} real",
            )
            # Synthetic
            mask_s = is_synth & (labs == g)
            ax.scatter(
                tsne[mask_s, 0], tsne[mask_s, 1],
                c=[colors[i]], s=30, alpha=0.5, marker="^",
                label=f"G{g} synth",
            )

        ax.set_title("Real vs Synthetic Data (Feature Space t-SNE)")
        ax.legend(fontsize=6, markerscale=1.5, ncol=3, loc="upper right")
        ax.set_xticks([])
        ax.set_yticks([])
        fig.tight_layout()
        self._save(fig, "fig_real_vs_synthetic_tsne")

    # 4. Subject Style Diversity Heatmap
    def plot_style_diversity(
        self,
        real_style_means: np.ndarray,
        synth_style_vectors: np.ndarray,
        real_subject_names: List[str],
        n_synth: int,
    ):
        """Heatmap of pairwise distances in style space."""
        n_real = len(real_style_means)
        all_styles = np.concatenate([real_style_means, synth_style_vectors], axis=0)
        all_names = list(real_subject_names) + [f"Synth_{i}" for i in range(n_synth)]

        dists = squareform(pdist(all_styles, metric="euclidean"))

        fig, ax = plt.subplots(figsize=(10, 8))
        im = ax.imshow(dists, cmap="YlOrRd", aspect="auto")
        ax.set_xticks(range(len(all_names)))
        ax.set_yticks(range(len(all_names)))
        ax.set_xticklabels(all_names, rotation=45, ha="right", fontsize=7)
        ax.set_yticklabels(all_names, fontsize=7)

        # Draw border between real and synthetic
        ax.axhline(n_real - 0.5, color="blue", linewidth=2, linestyle="--")
        ax.axvline(n_real - 0.5, color="blue", linewidth=2, linestyle="--")

        plt.colorbar(im, ax=ax, label="Euclidean Distance")
        ax.set_title("Style Space Pairwise Distances\n(Real vs Synthetic Subjects)")
        fig.tight_layout()
        self._save(fig, "fig_style_diversity")

    # 5. Cross-Subject Gap Analysis
    def plot_cross_subject_gap(
        self,
        subjects: List[str],
        baseline_accs: List[Optional[float]],
        augmented_accs: List[Optional[float]],
    ):
        """Per-subject accuracy comparison: baseline vs augmented."""
        fig, ax = plt.subplots(figsize=(12, 5))
        x = np.arange(len(subjects))
        width = 0.35

        baseline_vals = [a if a is not None else 0 for a in baseline_accs]
        augmented_vals = [a if a is not None else 0 for a in augmented_accs]

        bars1 = ax.bar(x - width / 2, baseline_vals, width, label="Baseline (real only)",
                       color="#E74C3C", alpha=0.8)
        bars2 = ax.bar(x + width / 2, augmented_vals, width, label="+ Synthetic subjects",
                       color="#2ECC71", alpha=0.8)

        # Value labels
        for bar in bars1:
            h = bar.get_height()
            if h > 0:
                ax.text(bar.get_x() + bar.get_width() / 2, h + 0.005,
                        f"{h:.3f}", ha="center", va="bottom", fontsize=7)
        for bar in bars2:
            h = bar.get_height()
            if h > 0:
                ax.text(bar.get_x() + bar.get_width() / 2, h + 0.005,
                        f"{h:.3f}", ha="center", va="bottom", fontsize=7)

        ax.set_xlabel("Test Subject")
        ax.set_ylabel("Accuracy")
        ax.set_title("Cross-Subject Gap: Baseline vs Domain Expansion")
        ax.set_xticks(x)
        ax.set_xticklabels(subjects, rotation=30, ha="right")
        ax.legend()
        ax.set_ylim(0, max(max(baseline_vals), max(augmented_vals)) * 1.15 + 0.01)
        ax.grid(axis="y", alpha=0.3)
        fig.tight_layout()
        self._save(fig, "fig_cross_subject_gap")

