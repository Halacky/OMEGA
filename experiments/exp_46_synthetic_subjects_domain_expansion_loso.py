# ===========================================================================
# Custom Trainer: SyntheticSubjectTrainer
# ===========================================================================

class SyntheticSubjectTrainer(WindowClassifierTrainer):
    """
    Three-phase trainer for domain expansion via synthetic subjects:

    Phase 1: Train Conditional VAE on all training windows.
    Phase 2: Generate synthetic subject data from trained VAE.
    Phase 3: Train downstream classifier on real + synthetic data.

    Additionally trains a baseline classifier (real only) for comparison.
    """

    def __init__(self, vae_cfg: dict, **kwargs):
        super().__init__(**kwargs)
        self.vae_cfg = vae_cfg
        self.viz = SyntheticSubjectVisualizer(self.output_dir, self.logger)

        # Stored after fit for later inspection
        self.vae_model: Optional[EMGConditionalVAE] = None
        self.vae_history: Optional[Dict] = None
        self.baseline_metrics: Optional[Dict] = None
        self.augmented_metrics: Optional[Dict] = None
        self.fold_extra: Dict = {}  # extra metrics for this fold

    # ------------------------------------------------------------------
    # Phase 1: Train VAE
    # ------------------------------------------------------------------

    def _train_vae(
        self,
        X_train: np.ndarray,   # (N, C, T) standardised
        y_train: np.ndarray,   # (N,) class indices (0-based)
        num_classes: int,
    ) -> Tuple[EMGConditionalVAE, Dict]:
        device = self.cfg.device
        cfg = self.vae_cfg
        in_channels = X_train.shape[1]
        time_steps = X_train.shape[2]

        vae = EMGConditionalVAE(
            in_channels=in_channels,
            time_steps=time_steps,
            num_classes=num_classes,
            content_dim=cfg["content_dim"],
            style_dim=cfg["style_dim"],
            gesture_embed_dim=cfg["gesture_embed_dim"],
            dropout=self.cfg.dropout,
        ).to(device)

        X_tensor = torch.from_numpy(X_train).float()
        y_tensor = torch.from_numpy(y_train).long()
        dataset = TensorDataset(X_tensor, y_tensor)
        loader = DataLoader(
            dataset,
            batch_size=cfg["vae_batch_size"],
            shuffle=True,
            num_workers=0,
            pin_memory=True,
        )

        optimizer = optim.AdamW(vae.parameters(), lr=cfg["vae_lr"], weight_decay=1e-4)
        scheduler = optim.lr_scheduler.CosineAnnealingLR(
            optimizer, T_max=cfg["vae_epochs"],
        )

        self.logger.info(
            f"[VAE] Training: {cfg['vae_epochs']} epochs, N={len(X_train)}, "
            f"content_dim={cfg['content_dim']}, style_dim={cfg['style_dim']}, "
            f"β_content={cfg['beta_content']}, β_style={cfg['beta_style']}"
        )

        history = {
            "total_loss": [], "recon_loss": [], "kl_content": [], "kl_style": [],
        }

        vae.train()
        for epoch in range(1, cfg["vae_epochs"] + 1):
            ep_total, ep_recon, ep_kl_c, ep_kl_s = 0.0, 0.0, 0.0, 0.0
            for batch_x, batch_y in loader:
                batch_x = batch_x.to(device)
                batch_y = batch_y.to(device)
                optimizer.zero_grad()

                x_recon, mu_c, logvar_c, mu_s, logvar_s = vae(batch_x, batch_y)
                loss, recon, kl_c, kl_s = vae.loss(
                    batch_x, x_recon, mu_c, logvar_c, mu_s, logvar_s,
                    beta_content=cfg["beta_content"],
                    beta_style=cfg["beta_style"],
                )
                loss.backward()
                nn.utils.clip_grad_norm_(vae.parameters(), 1.0)
                optimizer.step()

                bs = len(batch_x)
                ep_total += loss.item() * bs
                ep_recon += recon.item() * bs
                ep_kl_c += kl_c.item() * bs
                ep_kl_s += kl_s.item() * bs

            scheduler.step()
            n = len(X_train)
            history["total_loss"].append(ep_total / n)
            history["recon_loss"].append(ep_recon / n)
            history["kl_content"].append(ep_kl_c / n)
            history["kl_style"].append(ep_kl_s / n)

            if epoch % 10 == 0 or epoch == 1:
                self.logger.info(
                    f"[VAE] Epoch {epoch}/{cfg['vae_epochs']} — "
                    f"total={history['total_loss'][-1]:.5f}, "
                    f"recon={history['recon_loss'][-1]:.5f}, "
                    f"KL_c={history['kl_content'][-1]:.5f}, "
                    f"KL_s={history['kl_style'][-1]:.5f}"
                )

        return vae, history

    # ------------------------------------------------------------------
    # Phase 2: Generate synthetic data
    # ------------------------------------------------------------------

    def _generate_synthetic(
        self,
        vae: EMGConditionalVAE,
        X_train: np.ndarray,
        y_train: np.ndarray,
        num_classes: int,
    ) -> Tuple[np.ndarray, np.ndarray]:
        """Generate synthetic subject windows from trained VAE."""
        device = self.cfg.device
        cfg = self.vae_cfg

        self.logger.info(
            f"[Synth] Generating {cfg['n_synthetic_subjects']} synthetic subjects, "
            f"{cfg['synthetic_windows_per_gesture']} windows/gesture/subject"
        )

        X_t = torch.from_numpy(X_train).float()
        y_t = torch.from_numpy(y_train).long()

        synth_windows, synth_labels = vae.generate_from_real_styles(
            x_real=X_t,
            y_real=y_t,
            n_synthetic_subjects=cfg["n_synthetic_subjects"],
            windows_per_gesture=cfg["synthetic_windows_per_gesture"],
            device=device,
            style_noise_scale=cfg["style_noise_scale"],
        )

        synth_np = synth_windows.numpy()  # (M, C, T)
        labels_np = synth_labels.numpy()  # (M,)

        self.logger.info(
            f"[Synth] Generated: {synth_np.shape[0]} windows, shape {synth_np.shape}"
        )
        return synth_np, labels_np

    # ------------------------------------------------------------------
    # Train downstream classifier (shared helper)
    # ------------------------------------------------------------------

    def _train_classifier(
        self,
        X_train: np.ndarray,
        y_train: np.ndarray,
        X_val: np.ndarray,
        y_val: np.ndarray,
        num_classes: int,
        tag: str = "",
    ) -> nn.Module:
        """Train a CNN-GRU-Attention classifier and return the best model."""
        device = self.cfg.device
        in_channels = X_train.shape[1]

        model = self._create_model(
            in_channels=in_channels,
            num_classes=num_classes,
            model_type="cnn_gru_attention",
        ).to(device)

        dl_train = DataLoader(
            TensorDataset(
                torch.from_numpy(X_train).float(),
                torch.from_numpy(y_train).long(),
            ),
            batch_size=self.cfg.batch_size, shuffle=True,
            num_workers=0, pin_memory=True,
        )
        dl_val = None
        if len(X_val) > 0:
            dl_val = DataLoader(
                TensorDataset(
                    torch.from_numpy(X_val).float(),
                    torch.from_numpy(y_val).long(),
                ),
                batch_size=self.cfg.batch_size, shuffle=False,
                num_workers=0, pin_memory=True,
            )

        # Class weights
        if self.cfg.use_class_weights:
            counts = np.bincount(y_train, minlength=num_classes).astype(np.float32)
            weights = counts.sum() / (counts + 1e-8)
            weights /= weights.mean()
            criterion = nn.CrossEntropyLoss(
                weight=torch.from_numpy(weights).float().to(device),
            )
        else:
            criterion = nn.CrossEntropyLoss()

        optimizer = optim.AdamW(
            model.parameters(), lr=self.cfg.learning_rate, weight_decay=self.cfg.weight_decay,
        )
        scheduler = optim.lr_scheduler.ReduceLROnPlateau(
            optimizer, mode="min", patience=3, factor=0.5,
        )

        best_val_loss = float("inf")
        best_state = None
        patience_counter = 0

        for epoch in range(1, self.cfg.epochs + 1):
            model.train()
            train_loss, train_correct = 0.0, 0
            for bx, by in dl_train:
                bx, by = bx.to(device), by.to(device)
                optimizer.zero_grad()
                logits = model(bx)
                loss = criterion(logits, by)
                loss.backward()
                nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                optimizer.step()
                train_loss += loss.item() * len(bx)
                train_correct += (logits.argmax(1) == by).sum().item()

            train_loss /= len(y_train)
            train_acc = train_correct / len(y_train)

            if dl_val is not None:
                model.eval()
                val_loss, val_correct = 0.0, 0
                with torch.no_grad():
                    for bx, by in dl_val:
                        bx, by = bx.to(device), by.to(device)
                        logits = model(bx)
                        val_loss += criterion(logits, by).item() * len(bx)
                        val_correct += (logits.argmax(1) == by).sum().item()
                val_loss /= len(y_val)
                val_acc = val_correct / len(y_val)
                scheduler.step(val_loss)

                if val_loss < best_val_loss:
                    best_val_loss = val_loss
                    best_state = {k: v.cpu().clone() for k, v in model.state_dict().items()}
                    patience_counter = 0
                else:
                    patience_counter += 1

                if epoch % 10 == 0 or epoch == 1:
                    self.logger.info(
                        f"[{tag}] Epoch {epoch}/{self.cfg.epochs} "
                        f"train_loss={train_loss:.4f} acc={train_acc:.4f} "
                        f"val_loss={val_loss:.4f} val_acc={val_acc:.4f}"
                    )

                if patience_counter >= self.cfg.early_stopping_patience:
                    self.logger.info(f"[{tag}] Early stopping at epoch {epoch}")
                    break
            else:
                if epoch % 10 == 0 or epoch == 1:
                    self.logger.info(
                        f"[{tag}] Epoch {epoch}/{self.cfg.epochs} "
                        f"train_loss={train_loss:.4f} acc={train_acc:.4f}"
                    )

        if best_state is not None:
            model.load_state_dict(best_state)

        return model

    # ------------------------------------------------------------------
    # Evaluate helper
    # ------------------------------------------------------------------

    def _evaluate_model(
        self,
        model: nn.Module,
        X: np.ndarray,
        y: np.ndarray,
        tag: str = "",
    ) -> Dict:
        """Evaluate a model on (N, C, T) data, return metrics dict."""
        device = self.cfg.device
        model.eval()

        X_t = torch.from_numpy(X).float().to(device)
        all_preds = []
        bs = self.cfg.batch_size
        with torch.no_grad():
            for i in range(0, len(X_t), bs):
                logits = model(X_t[i:i + bs])
                all_preds.append(logits.argmax(1).cpu().numpy())

        preds = np.concatenate(all_preds)
        acc = accuracy_score(y, preds)
        f1 = f1_score(y, preds, average="macro", zero_division=0)
        report = classification_report(y, preds, output_dict=True, zero_division=0)
        cm = confusion_matrix(y, preds)

        self.logger.info(f"[{tag}] Accuracy={acc:.4f}, F1-macro={f1:.4f}")
        return {
            "accuracy": float(acc),
            "f1_macro": float(f1),
            "report": report,
            "confusion_matrix": cm.tolist(),
        }

    # ------------------------------------------------------------------
    # Latent-space analysis & visualizations
    # ------------------------------------------------------------------

    def _compute_latent_representations(
        self,
        vae: EMGConditionalVAE,
        X: np.ndarray,
        y: np.ndarray,
        subject_ids: Optional[np.ndarray] = None,
    ) -> Dict:
        """Encode data, return mu_content, mu_style + labels."""
        device = self.cfg.device
        vae.eval()

        all_mu_c, all_mu_s = [], []
        bs = 512
        X_t = torch.from_numpy(X).float()
        y_t = torch.from_numpy(y).long()

        with torch.no_grad():
            for i in range(0, len(X_t), bs):
                mu_c, _, mu_s, _ = vae.encode(
                    X_t[i:i + bs].to(device), y_t[i:i + bs].to(device),
                )
                all_mu_c.append(mu_c.cpu().numpy())
                all_mu_s.append(mu_s.cpu().numpy())

        return {
            "mu_content": np.concatenate(all_mu_c, axis=0),
            "mu_style": np.concatenate(all_mu_s, axis=0),
            "gesture_labels": y,
            "subject_labels": subject_ids if subject_ids is not None else np.zeros(len(y)),
        }

    def _compute_feature_fid(
        self, real_windows: np.ndarray, synth_windows: np.ndarray,
    ) -> float:
        """Compute FID-like metric using raw signal statistics."""
        # Use per-channel statistics as features
        def extract_stats(W):
            # (N, C, T)
            means = W.mean(axis=2)       # (N, C)
            stds = W.std(axis=2)          # (N, C)
            rms = np.sqrt((W ** 2).mean(axis=2))  # (N, C)
            return np.concatenate([means, stds, rms], axis=1)  # (N, 3C)

        f_real = extract_stats(real_windows)
        f_synth = extract_stats(synth_windows)

        mu1, mu2 = f_real.mean(axis=0), f_synth.mean(axis=0)
        sigma1 = np.cov(f_real, rowvar=False) + np.eye(f_real.shape[1]) * 1e-6
        sigma2 = np.cov(f_synth, rowvar=False) + np.eye(f_synth.shape[1]) * 1e-6

        return compute_frechet_distance(mu1, sigma1, mu2, sigma2)

    # ------------------------------------------------------------------
    # Overridden fit()
    # ------------------------------------------------------------------

    def fit(self, splits: Dict) -> Dict:
        """
        Three-phase fit:
          1. Prepare data, train VAE.
          2. Generate synthetic subjects.
          3. Train baseline (real only) and augmented (real + synth) classifiers.
          4. Generate all visualizations.
        """
        seed_everything(self.cfg.seed)

        # --- Prepare flat arrays ---
        X_train, y_train, X_val, y_val, X_test, y_test, class_ids, class_names = \
            self._prepare_splits_arrays(splits)

        # Transpose (N, T, C) → (N, C, T)
        def _t(X):
            if isinstance(X, np.ndarray) and X.ndim == 3 and X.shape[1] > X.shape[2]:
                return np.transpose(X, (0, 2, 1))
            return X

        X_train = _t(X_train)
        X_val = _t(X_val) if len(X_val) > 0 else X_val
        X_test = _t(X_test) if len(X_test) > 0 else X_test

        in_channels = X_train.shape[1]
        time_steps = X_train.shape[2]
        num_classes = len(class_ids)

        # Channel standardisation
        mean_c, std_c = self._compute_channel_standardization(X_train)
        self.mean_c = mean_c
        self.std_c = std_c
        self.class_ids = class_ids
        self.class_names = class_names
        self.in_channels = in_channels
        self.window_size = time_steps

        X_train_n = self._apply_standardization(X_train, mean_c, std_c)
        X_val_n = self._apply_standardization(X_val, mean_c, std_c) if len(X_val) > 0 else X_val
        X_test_n = self._apply_standardization(X_test, mean_c, std_c) if len(X_test) > 0 else X_test

        # Build subject labels for latent-space analysis
        # (CrossSubjectExperiment pools train subjects; we try to recover
        #  per-subject info from the splits if available, else use zeros)
        subject_labels_train = np.zeros(len(y_train), dtype=int)
        if "train_subject_labels" in splits:
            # Build flat array from dict structure
            subj_arrs = []
            for g_id in class_ids:
                if g_id in splits["train_subject_labels"]:
                    subj_arrs.append(splits["train_subject_labels"][g_id])
            if subj_arrs:
                subject_labels_train = np.concatenate(subj_arrs)
                if len(subject_labels_train) != len(y_train):
                    subject_labels_train = np.zeros(len(y_train), dtype=int)

        # =====================================================================
        # Phase 1: Train VAE
        # =====================================================================
        self.logger.info("=" * 60)
        self.logger.info("PHASE 1: Training Conditional VAE")
        self.logger.info("=" * 60)

        # Combine train + val for VAE training (more data, unsupervised)
        if len(X_val_n) > 0:
            X_vae = np.concatenate([X_train_n, X_val_n], axis=0)
            y_vae = np.concatenate([y_train, y_val], axis=0)
        else:
            X_vae = X_train_n
            y_vae = y_train

        vae, vae_history = self._train_vae(X_vae, y_vae, num_classes)
        self.vae_model = vae
        self.vae_history = vae_history

        # Save VAE weights
        vae_path = self.output_dir / "vae_model.pt"
        torch.save(vae.state_dict(), vae_path)
        self.logger.info(f"[VAE] Model saved: {vae_path}")

        # Vis 7: VAE training curves
        self.viz.plot_vae_training_curves(vae_history)

        # =====================================================================
        # Phase 1b: VAE analysis & visualizations
        # =====================================================================

        # Reconstruct some training samples for visualization
        vae.eval()
        n_vis = min(500, len(X_train_n))
        with torch.no_grad():
            X_vis = torch.from_numpy(X_train_n[:n_vis]).float().to(self.cfg.device)
            y_vis = torch.from_numpy(y_train[:n_vis]).long().to(self.cfg.device)
            x_recon_vis, _, _, _, _ = vae(X_vis, y_vis)
            x_recon_np = x_recon_vis.cpu().numpy()

        # Vis 1: Reconstruction quality
        self.viz.plot_vae_reconstruction(
            X_train_n[:n_vis], x_recon_np, y_train[:n_vis], class_ids,
        )

        # Compute latent representations for visualizations
        latent = self._compute_latent_representations(
            vae, X_train_n, y_train, subject_labels_train,
        )

        # Vis 2: Latent space t-SNE
        self.viz.plot_latent_space_tsne(
            latent["mu_content"], latent["mu_style"],
            latent["gesture_labels"], latent["subject_labels"],
        )

        # =====================================================================
        # Phase 2: Generate synthetic subjects
        # =====================================================================
        self.logger.info("=" * 60)
        self.logger.info("PHASE 2: Generating Synthetic Subjects")
        self.logger.info("=" * 60)

        synth_windows, synth_labels = self._generate_synthetic(
            vae, X_train_n, y_train, num_classes,
        )

        # Apply same standardisation to synthetic (they're generated from
        # standardised input, but clamp for safety)
        synth_windows = np.clip(synth_windows, -10, 10)

        # Vis 8: Synthetic quality metrics
        feature_fid = self._compute_feature_fid(X_train_n, synth_windows)
        self.logger.info(f"[Synth] Feature FID = {feature_fid:.2f}")
        self.viz.plot_synthetic_quality(X_train_n, synth_windows, feature_fid)

        # Vis 4: Style diversity heatmap
        # Compute per-subject mean styles
        unique_subjects = np.unique(subject_labels_train)
        real_style_means = []
        train_subject_names = []
        for s_idx in unique_subjects:
            mask_s = subject_labels_train == s_idx
            if mask_s.sum() > 0:
                real_style_means.append(latent["mu_style"][mask_s].mean(axis=0))
                train_subject_names.append(f"Real_{s_idx}")
        if real_style_means:
            real_style_means_arr = np.stack(real_style_means)

            # Compute synthetic subject style vectors
            synth_style_vectors = []
            cfg = self.vae_cfg
            n_synth = cfg["n_synthetic_subjects"]
            real_mean = latent["mu_style"].mean(axis=0)
            real_std = latent["mu_style"].std(axis=0)
            rng = np.random.RandomState(42)
            for i in range(n_synth):
                sv = real_mean + real_std * rng.randn(len(real_mean)) + \
                     rng.randn(len(real_mean)) * cfg["style_noise_scale"]
                synth_style_vectors.append(sv)
            synth_style_arr = np.stack(synth_style_vectors)

            self.viz.plot_style_diversity(
                real_style_means_arr, synth_style_arr,
                train_subject_names, n_synth,
            )

        # Vis 9: Style interpolation (between first two real subject groups)
        try:
            if len(unique_subjects) >= 2:
                s_a, s_b = unique_subjects[0], unique_subjects[1]
                mask_a = subject_labels_train == s_a
                mask_b = subject_labels_train == s_b
                if mask_a.sum() > 10 and mask_b.sum() > 10:
                    g_for_interp = class_ids[0]
                    g_mask_a = mask_a & (y_train == g_for_interp)
                    g_mask_b = mask_b & (y_train == g_for_interp)
                    if g_mask_a.sum() > 0 and g_mask_b.sum() > 0:
                        interp = vae.interpolate_styles(
                            torch.from_numpy(X_train_n[g_mask_a][:20]).float(),
                            torch.from_numpy(y_train[g_mask_a][:20]).long(),
                            torch.from_numpy(X_train_n[g_mask_b][:20]).float(),
                            torch.from_numpy(y_train[g_mask_b][:20]).long(),
                            gesture_label=0,  # 0-indexed class
                            steps=8,
                            device=self.cfg.device,
                        )
                        self.viz.plot_style_interpolation(interp.numpy(), g_for_interp)
        except Exception as e:
            self.logger.warning(f"Style interpolation viz failed: {e}")

        # Vis 3: Real vs Synthetic t-SNE (using raw signal stats as features)
        def _quick_features(W, max_n=2000):
            """Extract quick statistical features for t-SNE."""
            if len(W) > max_n:
                idx = np.random.RandomState(42).choice(len(W), max_n, replace=False)
                W = W[idx]
            means = W.mean(axis=2)
            stds = W.std(axis=2)
            rms = np.sqrt((W ** 2).mean(axis=2))
            maxs = np.abs(W).max(axis=2)
            return np.concatenate([means, stds, rms, maxs], axis=1)

        feats_real = _quick_features(X_train_n)
        feats_synth = _quick_features(synth_windows)
        n_r = len(feats_real)
        n_s = len(feats_synth)
        # Need matching label arrays
        if n_r <= len(y_train):
            labs_r = y_train[:n_r]
        else:
            labs_r = y_train
        labs_s = synth_labels[:n_s]

        self.viz.plot_real_vs_synthetic_tsne(feats_real, labs_r, feats_synth, labs_s)

        # =====================================================================
        # Phase 3: Train classifiers (baseline + augmented)
        # =====================================================================
        self.logger.info("=" * 60)
        self.logger.info("PHASE 3a: Training BASELINE classifier (real data only)")
        self.logger.info("=" * 60)

        baseline_model = self._train_classifier(
            X_train_n, y_train, X_val_n, y_val, num_classes, tag="Baseline",
        )

        self.logger.info("=" * 60)
        self.logger.info("PHASE 3b: Training AUGMENTED classifier (real + synthetic)")
        self.logger.info("=" * 60)

        # Combine real + synthetic
        X_aug = np.concatenate([X_train_n, synth_windows], axis=0)
        y_aug = np.concatenate([y_train, synth_labels], axis=0)

        self.logger.info(
            f"[Aug] Training data: {len(X_train_n)} real + {len(synth_windows)} synth "
            f"= {len(X_aug)} total"
        )

        augmented_model = self._train_classifier(
            X_aug, y_aug, X_val_n, y_val, num_classes, tag="Augmented",
        )

        # =====================================================================
        # Evaluate both on test set
        # =====================================================================
        if len(X_test_n) > 0:
            self.logger.info("=" * 60)
            self.logger.info("Evaluating both classifiers on test data")
            self.logger.info("=" * 60)

            baseline_metrics = self._evaluate_model(
                baseline_model, X_test_n, y_test, tag="Baseline-Test",
            )
            augmented_metrics = self._evaluate_model(
                augmented_model, X_test_n, y_test, tag="Augmented-Test",
            )

            self.baseline_metrics = baseline_metrics
            self.augmented_metrics = augmented_metrics

            delta_acc = augmented_metrics["accuracy"] - baseline_metrics["accuracy"]
            delta_f1 = augmented_metrics["f1_macro"] - baseline_metrics["f1_macro"]

            self.logger.info(
                f"[Result] Baseline: Acc={baseline_metrics['accuracy']:.4f}, "
                f"F1={baseline_metrics['f1_macro']:.4f}"
            )
            self.logger.info(
                f"[Result] Augmented: Acc={augmented_metrics['accuracy']:.4f}, "
                f"F1={augmented_metrics['f1_macro']:.4f}"
            )
            self.logger.info(
                f"[Result] Delta: Acc={delta_acc:+.4f}, F1={delta_f1:+.4f}"
            )

            # Vis 6: Per-gesture F1 delta
            baseline_report = baseline_metrics.get("report", {})
            augmented_report = augmented_metrics.get("report", {})
            baseline_per_gesture = {
                str(c): baseline_report.get(str(c), {}).get("f1-score", 0.0)
                for c in range(num_classes)
            }
            augmented_per_gesture = {
                str(c): augmented_report.get(str(c), {}).get("f1-score", 0.0)
                for c in range(num_classes)
            }
            self.viz.plot_per_gesture_delta(
                list(range(num_classes)), baseline_per_gesture, augmented_per_gesture,
            )

            # Save comparison metrics
            self.fold_extra = {
                "baseline_accuracy": baseline_metrics["accuracy"],
                "baseline_f1_macro": baseline_metrics["f1_macro"],
                "augmented_accuracy": augmented_metrics["accuracy"],
                "augmented_f1_macro": augmented_metrics["f1_macro"],
                "delta_accuracy": delta_acc,
                "delta_f1_macro": delta_f1,
                "feature_fid": feature_fid,
                "n_real_train": len(X_train_n),
                "n_synthetic": len(synth_windows),
                "n_total_augmented": len(X_aug),
                "vae_final_recon_loss": vae_history["recon_loss"][-1],
                "vae_final_kl_content": vae_history["kl_content"][-1],
                "vae_final_kl_style": vae_history["kl_style"][-1],
            }

            # Save fold comparison
            with open(self.output_dir / "fold_comparison.json", "w") as f:
                json.dump(make_json_serializable(self.fold_extra), f, indent=4)

        # The "main" model is the augmented one (for CrossSubjectExperiment.evaluate_numpy)
        self.model = augmented_model

        # Cleanup VAE to save memory
        del vae
        if torch.cuda.is_available():
            torch.cuda.empty_cache()

        return {}


