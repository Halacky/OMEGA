"""
Experiment 46: Synthetic Subjects via Conditional VAE for Domain Expansion (LOSO)

Hypothesis: Expanding the subject space with synthetic EMG data generated by
a Conditional VAE reduces the cross-subject gap in LOSO evaluation.

The VAE disentangles gesture content from subject style in the latent space.
By sampling novel style vectors we create "virtual subjects" whose data
augments the training pool, increasing style diversity seen by the classifier.

Architecture:
  Phase 1 — Train Conditional VAE:
    Encoder: (B,C,T) → CNN → [μ_content, logvar_content, μ_style, logvar_style]
    Decoder: [z_content, z_style, gesture_emb] → TransposedCNN → (B,C,T)
    Loss: MSE_recon + β_c·KL_content + β_s·KL_style

  Phase 2 — Generate synthetic subjects:
    For each virtual subject: sample novel style vector, generate windows per gesture.

  Phase 3 — Train downstream classifier (CNN-GRU-Attention) on real + synthetic.
    Compare with baseline (real only) to measure the domain expansion benefit.

Baseline comparison: exp_1 (CNN-GRU-Attention, deep_raw, real data only)

Visualizations (10 types):
  1. VAE reconstruction quality
  2. Latent space t-SNE (content/style × gesture/subject)
  3. Real vs synthetic feature-space t-SNE
  4. Subject style diversity heatmap
  5. Cross-subject gap analysis (baseline vs augmented)
  6. Per-gesture accuracy delta
  7. VAE training curves
  8. Synthetic data quality metrics
  9. Style interpolation
  10. Aggregate LOSO summary
"""

