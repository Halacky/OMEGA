    # 8. Synthetic Data Quality Metrics
    def plot_synthetic_quality(
        self,
        real_windows: np.ndarray,
        synth_windows: np.ndarray,
        feature_fid: float,
    ):
        """Compare distributions of real vs synthetic: amplitude stats + autocorrelation."""
        fig, axes = plt.subplots(1, 3, figsize=(16, 5))
        fig.suptitle(
            f"Synthetic Data Quality (Feature FID = {feature_fid:.2f})", fontsize=14,
        )

        n_channels = real_windows.shape[1]

        # (a) Per-channel RMS violin
        real_rms = np.sqrt(np.mean(real_windows ** 2, axis=2))   # (N, C)
        synth_rms = np.sqrt(np.mean(synth_windows ** 2, axis=2))

        positions_r = np.arange(n_channels) * 3
        positions_s = np.arange(n_channels) * 3 + 1

        vp_r = axes[0].violinplot(
            [real_rms[:, ch] for ch in range(n_channels)],
            positions=positions_r, showmeans=True, widths=0.8,
        )
        vp_s = axes[0].violinplot(
            [synth_rms[:, ch] for ch in range(n_channels)],
            positions=positions_s, showmeans=True, widths=0.8,
        )
        for body in vp_r["bodies"]:
            body.set_facecolor("#3498DB")
            body.set_alpha(0.7)
        for body in vp_s["bodies"]:
            body.set_facecolor("#E67E22")
            body.set_alpha(0.7)

        axes[0].set_xticks(positions_r + 0.5)
        axes[0].set_xticklabels([f"Ch{i}" for i in range(n_channels)], fontsize=7)
        axes[0].set_title("Per-Channel RMS")
        axes[0].set_ylabel("RMS Amplitude")

        # Legend
        from matplotlib.patches import Patch
        axes[0].legend(
            handles=[Patch(color="#3498DB", alpha=0.7, label="Real"),
                     Patch(color="#E67E22", alpha=0.7, label="Synthetic")],
            fontsize=8,
        )

        # (b) Amplitude histogram (flattened)
        real_flat = real_windows.flatten()
        synth_flat = synth_windows.flatten()
        # Subsample for speed
        rng = np.random.RandomState(42)
        n_hist = min(500_000, len(real_flat))
        axes[1].hist(
            rng.choice(real_flat, n_hist, replace=False),
            bins=100, alpha=0.6, density=True, color="#3498DB", label="Real",
        )
        axes[1].hist(
            rng.choice(synth_flat, min(n_hist, len(synth_flat)), replace=False),
            bins=100, alpha=0.6, density=True, color="#E67E22", label="Synthetic",
        )
        axes[1].set_title("Amplitude Distribution")
        axes[1].set_xlabel("Amplitude")
        axes[1].set_ylabel("Density")
        axes[1].legend(fontsize=8)

        # (c) Temporal autocorrelation (average over channels, first 50 lags)
        max_lag = min(50, real_windows.shape[2] // 2)

        def avg_autocorr(windows, max_lag):
            # Use first 200 windows for speed
            w = windows[:min(200, len(windows)), 0, :]  # first channel
            ac = np.zeros(max_lag)
            for lag in range(max_lag):
                if lag == 0:
                    ac[lag] = 1.0
                else:
                    x1 = w[:, :-lag]
                    x2 = w[:, lag:]
                    ac[lag] = np.mean(
                        np.sum(x1 * x2, axis=1) /
                        (np.sqrt(np.sum(x1 ** 2, axis=1) * np.sum(x2 ** 2, axis=1)) + 1e-10)
                    )
            return ac

        ac_real = avg_autocorr(real_windows, max_lag)
        ac_synth = avg_autocorr(synth_windows, max_lag)
        axes[2].plot(ac_real, "b-", linewidth=1.5, label="Real")
        axes[2].plot(ac_synth, "r--", linewidth=1.5, label="Synthetic")
        axes[2].set_title("Temporal Autocorrelation (Ch0)")
        axes[2].set_xlabel("Lag")
        axes[2].set_ylabel("Correlation")
        axes[2].legend(fontsize=8)
        axes[2].grid(alpha=0.3)

        fig.tight_layout()
        self._save(fig, "fig_synthetic_quality")

