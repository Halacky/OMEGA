def main():
    EXPERIMENT_NAME = "exp_46_synthetic_subjects_domain_expansion_loso"
    BASE_DIR = ROOT / "data"
    ALL_SUBJECTS = parse_subjects_args()
    OUTPUT_DIR = Path(
        f"./experiments_output/{EXPERIMENT_NAME}_"
        + "_".join(s.split("_s")[1] for s in ALL_SUBJECTS)
    )

    EXERCISES = ["E1"]
    MODEL_TYPES = ["cnn_gru_attention"]

    proc_cfg = ProcessingConfig(
        window_size=400,
        window_overlap=200,
        num_channels=8,
        sampling_rate=2000,
        segment_edge_margin=0.1,
    )

    split_cfg = SplitConfig(
        train_ratio=0.7,
        val_ratio=0.15,
        test_ratio=0.15,
        mode="by_segments",
        shuffle_segments=True,
        seed=42,
        include_rest_in_splits=False,
    )

    train_cfg = TrainingConfig(
        batch_size=256,
        epochs=50,
        learning_rate=1e-3,
        weight_decay=1e-4,
        dropout=0.2,
        early_stopping_patience=7,
        use_class_weights=True,
        seed=42,
        num_workers=0,
        device="cuda" if torch.cuda.is_available() else "cpu",
        use_handcrafted_features=False,
        pipeline_type="deep_raw",
    )

    vae_cfg = dict(VAE_CFG)

    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    global_logger = setup_logging(OUTPUT_DIR)

    global_logger.info("=" * 80)
    global_logger.info(f"EXPERIMENT: {EXPERIMENT_NAME}")
    global_logger.info(f"Subjects ({len(ALL_SUBJECTS)}): {ALL_SUBJECTS}")
    global_logger.info(f"Device: {train_cfg.device}")
    global_logger.info(f"VAE config: {vae_cfg}")
    global_logger.info("=" * 80)

    all_loso_results = []

    for model_type in MODEL_TYPES:
        print(f"\nMODEL: {model_type} — LOSO over {len(ALL_SUBJECTS)} subjects")
        for test_subject in ALL_SUBJECTS:
            print(f"  LOSO fold: test_subject={test_subject}")
            train_subjects = [s for s in ALL_SUBJECTS if s != test_subject]
            fold_output_dir = OUTPUT_DIR / model_type / f"test_{test_subject}"

            try:
                fold_res = run_single_loso_fold(
                    base_dir=BASE_DIR,
                    output_dir=fold_output_dir,
                    train_subjects=train_subjects,
                    test_subject=test_subject,
                    exercises=EXERCISES,
                    model_type=model_type,
                    proc_cfg=proc_cfg,
                    split_cfg=split_cfg,
                    train_cfg=train_cfg,
                    vae_cfg=dict(vae_cfg),
                )
                all_loso_results.append(fold_res)

                b_acc = fold_res.get("baseline_accuracy")
                a_acc = fold_res.get("augmented_accuracy")
                d_acc = fold_res.get("delta_accuracy")
                b_str = f"{b_acc:.4f}" if b_acc is not None else "N/A"
                a_str = f"{a_acc:.4f}" if a_acc is not None else "N/A"
                d_str = f"{d_acc:+.4f}" if d_acc is not None else "N/A"
                print(f"  -> Baseline={b_str}, Augmented={a_str}, Delta={d_str}")

            except Exception as e:
                global_logger.error(f"Failed fold test={test_subject}: {e}")
                global_logger.error(traceback.format_exc())
                all_loso_results.append({
                    "test_subject": test_subject,
                    "model_type": model_type,
                    "test_accuracy": None,
                    "test_f1_macro": None,
                    "baseline_accuracy": None,
                    "augmented_accuracy": None,
                    "delta_accuracy": None,
                    "error": str(e),
                })

    # ===================================================================
    # Aggregate results
    # ===================================================================
    aggregate_results = {}
    for model_type in MODEL_TYPES:
        model_results = [
            r for r in all_loso_results
            if r["model_type"] == model_type and r.get("augmented_accuracy") is not None
        ]
        if not model_results:
            continue

        baseline_accs = [r["baseline_accuracy"] for r in model_results
                         if r.get("baseline_accuracy") is not None]
        baseline_f1s = [r["baseline_f1_macro"] for r in model_results
                        if r.get("baseline_f1_macro") is not None]
        aug_accs = [r["augmented_accuracy"] for r in model_results
                    if r.get("augmented_accuracy") is not None]
        aug_f1s = [r["augmented_f1_macro"] for r in model_results
                   if r.get("augmented_f1_macro") is not None]
        delta_accs = [r["delta_accuracy"] for r in model_results
                      if r.get("delta_accuracy") is not None]
        delta_f1s = [r["delta_f1_macro"] for r in model_results
                     if r.get("delta_f1_macro") is not None]
        fids = [r["feature_fid"] for r in model_results
                if r.get("feature_fid") is not None]

        aggregate_results[model_type] = {
            "baseline_mean_accuracy": float(np.mean(baseline_accs)) if baseline_accs else None,
            "baseline_std_accuracy": float(np.std(baseline_accs)) if baseline_accs else None,
            "baseline_mean_f1_macro": float(np.mean(baseline_f1s)) if baseline_f1s else None,
            "augmented_mean_accuracy": float(np.mean(aug_accs)) if aug_accs else None,
            "augmented_std_accuracy": float(np.std(aug_accs)) if aug_accs else None,
            "augmented_mean_f1_macro": float(np.mean(aug_f1s)) if aug_f1s else None,
            "mean_delta_accuracy": float(np.mean(delta_accs)) if delta_accs else None,
            "mean_delta_f1_macro": float(np.mean(delta_f1s)) if delta_f1s else None,
            "mean_feature_fid": float(np.mean(fids)) if fids else None,
            "num_subjects": len(model_results),
            "per_subject": model_results,
        }

        r = aggregate_results[model_type]
        b_mean = r.get("baseline_mean_accuracy")
        a_mean = r.get("augmented_mean_accuracy")
        d_mean = r.get("mean_delta_accuracy")

        b_str = f"{b_mean:.4f}" if b_mean is not None else "N/A"
        a_str = f"{a_mean:.4f}" if a_mean is not None else "N/A"
        d_str = f"{d_mean:+.4f}" if d_mean is not None else "N/A"

        print(f"\n{model_type}:")
        print(f"  Baseline: Acc={b_str}")
        print(f"  Augmented: Acc={a_str}")
        print(f"  Delta: {d_str}")
        global_logger.info(
            f"{model_type}: Baseline={b_str}, Augmented={a_str}, Delta={d_str}"
        )

    # ===================================================================
    # Aggregate visualizations
    # ===================================================================
    agg_viz = SyntheticSubjectVisualizer(OUTPUT_DIR, global_logger)

    for model_type in MODEL_TYPES:
        r = aggregate_results.get(model_type)
        if r is None:
            continue
        per_subj = r["per_subject"]

        subjects = [p["test_subject"] for p in per_subj]
        b_accs = [p.get("baseline_accuracy") for p in per_subj]
        a_accs = [p.get("augmented_accuracy") for p in per_subj]
        b_f1s = [p.get("baseline_f1_macro", 0.0) for p in per_subj]
        a_f1s = [p.get("augmented_f1_macro", 0.0) for p in per_subj]

        # Vis 5: Cross-subject gap
        agg_viz.plot_cross_subject_gap(subjects, b_accs, a_accs)

        # Vis 10: Aggregate LOSO summary
        valid_b_accs = [a for a in b_accs if a is not None]
        valid_a_accs = [a for a in a_accs if a is not None]
        valid_b_f1s = [f for f in b_f1s if f is not None]
        valid_a_f1s = [f for f in a_f1s if f is not None]
        if valid_b_accs and valid_a_accs:
            agg_viz.plot_loso_summary(
                subjects, valid_b_accs, valid_b_f1s, valid_a_accs, valid_a_f1s,
            )

    # ===================================================================
    # Save summary
    # ===================================================================
    summary = {
        "experiment_name": EXPERIMENT_NAME,
        "hypothesis": "Expanding subject space with VAE-generated synthetic subjects "
                      "reduces cross-subject gap in LOSO evaluation.",
        "subjects": ALL_SUBJECTS,
        "exercises": EXERCISES,
        "vae_cfg": vae_cfg,
        "processing_config": asdict(proc_cfg),
        "split_config": asdict(split_cfg),
        "training_config": asdict(train_cfg),
        "aggregate_results": aggregate_results,
        "individual_results": all_loso_results,
        "experiment_date": datetime.now().isoformat(),
    }
    summary_path = OUTPUT_DIR / "loso_summary.json"
    with open(summary_path, "w") as f:
        json.dump(make_json_serializable(summary), f, indent=4, ensure_ascii=False)

    global_logger.info(f"EXPERIMENT COMPLETE. Results: {OUTPUT_DIR.resolve()}")
    print(f"\nResults saved to: {OUTPUT_DIR.resolve()}")

    # ===================================================================
    # Hypothesis executor integration
    # ===================================================================
    try:
        from hypothesis_executor import mark_hypothesis_verified, mark_hypothesis_failed

        valid_results = [
            r for r in all_loso_results if r.get("augmented_accuracy") is not None
        ]
        if valid_results:
            aug_accs = [r["augmented_accuracy"] for r in valid_results]
            baseline_accs = [r["baseline_accuracy"] for r in valid_results
                             if r.get("baseline_accuracy") is not None]
            delta_accs = [r["delta_accuracy"] for r in valid_results
                          if r.get("delta_accuracy") is not None]

            mark_hypothesis_verified(
                hypothesis_id="H_synthetic_subjects",
                metrics={
                    "augmented_mean_accuracy": float(np.mean(aug_accs)),
                    "baseline_mean_accuracy": float(np.mean(baseline_accs)) if baseline_accs else None,
                    "mean_delta_accuracy": float(np.mean(delta_accs)) if delta_accs else None,
                    "num_folds": len(valid_results),
                },
                experiment_name=EXPERIMENT_NAME,
            )
        else:
            mark_hypothesis_failed(
                hypothesis_id="H_synthetic_subjects",
                error_message="All LOSO folds failed — no valid results.",
            )
    except ImportError:
        pass


if __name__ == "__main__":
    main()
