def run_single_loso_fold(
    base_dir: Path,
    output_dir: Path,
    train_subjects: List[str],
    test_subject: str,
    exercises: List[str],
    model_type: str,
    proc_cfg: ProcessingConfig,
    split_cfg: SplitConfig,
    train_cfg: TrainingConfig,
    vae_cfg: dict,
) -> Dict:
    output_dir.mkdir(parents=True, exist_ok=True)
    logger = setup_logging(output_dir)
    seed_everything(train_cfg.seed, verbose=False)

    train_cfg.model_type = model_type
    train_cfg.pipeline_type = "deep_raw"
    train_cfg.use_handcrafted_features = False

    proc_cfg.save(output_dir / "processing_config.json")
    train_cfg.save(output_dir / "training_config.json")
    with open(output_dir / "split_config.json", "w") as f:
        json.dump(asdict(split_cfg), f, indent=4)
    with open(output_dir / "vae_config.json", "w") as f:
        json.dump(vae_cfg, f, indent=4)

    cs_cfg = CrossSubjectConfig(
        train_subjects=train_subjects,
        test_subject=test_subject,
        exercises=exercises,
        base_dir=base_dir,
        pool_train_subjects=True,
        use_separate_val_subject=False,
        val_subject=None,
        val_ratio=0.15,
        seed=train_cfg.seed,
        max_gestures=10,
    )
    cs_cfg.save(output_dir / "cross_subject_config.json")

    multi_loader = MultiSubjectLoader(
        processing_config=proc_cfg,
        logger=logger,
        use_gpu=True,
        use_improved_processing=True,
    )

    from visualization.base import Visualizer

    base_viz = Visualizer(output_dir, logger)

    trainer = SyntheticSubjectTrainer(
        vae_cfg=vae_cfg,
        train_cfg=train_cfg,
        logger=logger,
        output_dir=output_dir,
        visualizer=base_viz,
    )

    experiment = CrossSubjectExperiment(
        cross_subject_config=cs_cfg,
        split_config=split_cfg,
        multi_subject_loader=multi_loader,
        trainer=trainer,
        visualizer=base_viz,
        logger=logger,
    )

    try:
        results = experiment.run()
    except Exception as e:
        logger.error(f"Error in LOSO fold (test={test_subject}): {e}")
        traceback.print_exc()
        return {
            "test_subject": test_subject,
            "model_type": model_type,
            "test_accuracy": None,
            "test_f1_macro": None,
            "baseline_accuracy": None,
            "baseline_f1_macro": None,
            "augmented_accuracy": None,
            "augmented_f1_macro": None,
            "delta_accuracy": None,
            "delta_f1_macro": None,
            "error": str(e),
        }

    test_metrics = results.get("cross_subject_test", {})
    test_acc = float(test_metrics.get("accuracy", 0.0))
    test_f1 = float(test_metrics.get("f1_macro", 0.0))

    # Get the fold comparison from trainer
    fold_extra = trainer.fold_extra

    logger.info(
        f"[LOSO] Test={test_subject} | "
        f"Baseline Acc={fold_extra.get('baseline_accuracy', 'N/A')}, "
        f"Augmented Acc={fold_extra.get('augmented_accuracy', 'N/A')}, "
        f"Delta={fold_extra.get('delta_accuracy', 'N/A')}"
    )
    print(
        f"[LOSO] Test={test_subject} | "
        f"Baseline Acc={fold_extra.get('baseline_accuracy', 'N/A')}, "
        f"Augmented Acc={fold_extra.get('augmented_accuracy', 'N/A')}"
    )

    results_to_save = {k: v for k, v in results.items() if k != "subjects_data"}
    results_to_save["fold_comparison"] = fold_extra
    with open(output_dir / "cross_subject_results.json", "w") as f:
        json.dump(make_json_serializable(results_to_save), f, indent=4, ensure_ascii=False)

    saver = ArtifactSaver(output_dir, logger)
    saver.save_metadata(
        make_json_serializable({
            "test_subject": test_subject,
            "train_subjects": train_subjects,
            "model_type": model_type,
            "exercises": exercises,
            "vae_cfg": vae_cfg,
            "fold_comparison": fold_extra,
        }),
        filename="fold_metadata.json",
    )

    if torch.cuda.is_available():
        torch.cuda.empty_cache()
        torch.cuda.ipc_collect()
    del experiment, trainer, multi_loader, base_viz
    gc.collect()

    return {
        "test_subject": test_subject,
        "model_type": model_type,
        "test_accuracy": test_acc,
        "test_f1_macro": test_f1,
        "baseline_accuracy": fold_extra.get("baseline_accuracy"),
        "baseline_f1_macro": fold_extra.get("baseline_f1_macro"),
        "augmented_accuracy": fold_extra.get("augmented_accuracy"),
        "augmented_f1_macro": fold_extra.get("augmented_f1_macro"),
        "delta_accuracy": fold_extra.get("delta_accuracy"),
        "delta_f1_macro": fold_extra.get("delta_f1_macro"),
        "feature_fid": fold_extra.get("feature_fid"),
    }


